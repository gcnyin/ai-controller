"""提示词模板 —— 规划提示词与任务执行提示词。"""

import textwrap

PLAN_PROMPT = textwrap.dedent("""\
    仔细扫描整个代码库，生成一份按优先级排序的改进任务列表。

    ## 核心原则：聚焦高价值改动

    你的时间很宝贵，每一轮执行都应该产出实质性价值。请严格遵循以下优先级哲学：

    - **优先新功能开发和关键改进**：问问自己"这个改动能让用户多做什么事？"如果答案是"没什么区别"，它就不值得作为一个任务。
    - **忽略低价值事项**
    - **低优先级不等于低价值**：low 优先级保留给"值得做但不急"的真正改进，而非鸡毛蒜皮的小事。如果一件事做不做对项目没影响，直接不提。

    ## 改进类型（按价值从高到低排列）

    ### A. 功能开发类（最优先）
    缺少的重要功能、新特性、用户体验改进、让项目能做更多事情的能力扩展。

    ### B. 修复类（如有明显 bug）
    崩溃、数据丢失、逻辑错误、边界条件处理缺失等影响正确性的问题。

    ### C. 架构/质量类（有实质收益的重构）
    模块拆分、消除重复、改善可维护性或可测试性。仅当改动量足够大且收益明确时才列入。

    ### D. 性能优化类
    可测量的性能瓶颈修复。如果只是"理论上可以更快"但没有实际瓶颈证据，不要列入。

    ### E. 质量保障类
    补充测试覆盖关键路径、CI/CD 改进等。只为核心逻辑补测试，不为覆盖率达到某个数字而凑测试。

    ## 输出要求（重要）

    你必须使用以下 JSON 格式输出任务列表（不要包含 markdown 代码块标记，只输出纯 JSON）：

    {
      "tasks": [
        {"id": 1, "priority": "high", "type": "功能开发类", "title": "简短标题", "description": "详细描述要做什么、改哪个文件、为什么选这个"},
        {"id": 2, "priority": "medium", "type": "修复类", "title": "简短标题", "description": "详细描述"}
      ],
      "summary": "整体评估总结，用中文"
    }

    规则：
    - id 从 1 开始递增
    - priority 为 high / medium / low
    - high 和 medium 任务应占总数 80% 以上，low 只保留真正有价值但不急的事项
    - title 不超过 30 个字
    - description 要说清楚改什么文件、做什么改动、为什么这个改动有实际价值
    - 按优先级从高到低排列
    - 如果代码库已经很完善，tasks 可以为空数组
    - 鸡毛蒜皮的事情（格式化、重命名、注释微调等）一律不要出现
    - 禁止使用 emoji
    - 使用中文

    开始吧，先扫描代码库，然后生成完整的任务列表。
    """).strip()

TASK_PROMPT = textwrap.dedent("""\
    ## 当前任务

    {task_description}

    ## 行为准则

    - 直接修改文件，不要只给建议
    - 尽可能添加测试
    - 保持改动最小化，只做当前任务要求的事，不对无关部分动手
    - 确保改动后代码仍然可编译/可运行
    - 不改 .git/、node_modules/ 等非项目目录
    - 专注于做出有价值的实质改动。不要顺手调整代码风格、格式化、重命名等无关事项
    - 使用中文回复
    - 禁止使用 emoji

    改动完成后，在输出最后单独一行给出改动总结，格式：
    SUMMARY: <一句话中文说明你做了什么改动>
    """).strip()


def build_task_prompt(task: dict) -> str:
    """为单个任务构建执行提示词"""
    desc = f"**[{task.get('type', '改进')}] {task.get('title', '')}**\n\n{task.get('description', '')}"
    return TASK_PROMPT.format(task_description=desc)
